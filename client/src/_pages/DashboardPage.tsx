/* eslint-disable @typescript-eslint/no-unused-vars */

import { useEffect, useState } from 'react';
import styled from 'styled-components';
import AddWorkout from '../_components/cards/AddWorkout';
import CategoryChart from '../_components/cards/CategoryChart';
import CountsCard from '../_components/cards/CountsCard';
import WeeklyStatCard from '../_components/cards/WeeklyStatCard';
import WorkoutCard from '../_components/cards/WorkoutCard';
import { addWorkout, getDashboardDetails, getWorkouts } from '../_state/api';
import { counts } from '../utils/data';

const Container = styled.div`
  flex: 1;
  height: 100%;
  display: flex;
  justify-content: center;
  padding: 22px 0px;
  overflow-y: scroll;
`;

const Wrapper = styled.div`
  flex: 1;
  max-width: 1400px;
  display: flex;
  flex-direction: column;
  gap: 22px;
  @media (max-width: 600px) {
    gap: 12px;
  }
`;

const Title = styled.div`
  padding: 0px 16px;
  font-size: 22px;
  color: ${({ theme }) => theme.text_primary};
  font-weight: 500;
`;

const FlexWrap = styled.div`
  display: flex;
  flex-wrap: wrap;
  justify-content: space-between;
  gap: 22px;
  padding: 0px 16px;
  @media (max-width: 600px) {
    gap: 12px;
  }
`;

const Section = styled.div`
  display: flex;
  flex-direction: column;
  padding: 0px 16px;
  gap: 22px;
  padding: 0px 16px;
  @media (max-width: 600px) {
    gap: 12px;
  }
`;

const CardWrapper = styled.div`
  display: flex;
  flex-wrap: wrap;
  justify-content: center;
  gap: 20px;
  margin-bottom: 100px;
  @media (max-width: 600px) {
    gap: 12px;
  }
`;

const DashboardPage = () => {
  const [loading, setLoading] = useState<boolean>(false);
  const [data, setData] = useState();
  const [buttonLoading, setButtonLoading] = useState<boolean>(false);
  const [todaysWorkouts, setTodaysWorkouts] = useState([]);
  const [workout, setWorkout] = useState<string>(`#Legs
-Back Squat
-5 setsX15 reps
-30 kg
-10 min`);

  const dashboardData = async () => {
    setLoading(true);
    const token = localStorage.getItem('fitness-exercise-tracker-token');
    await getDashboardDetails(token as string).then((res) => {
      setData(res?.data?.data);
      console.log(res.data);
      setLoading(false);
    });
  };

  const getTodaysWorkout = async () => {
    setLoading(true);
    const token = localStorage.getItem('fitness-exercise-tracker-token');
    await getWorkouts(token as string, '').then((res) => {
      setTodaysWorkouts(res?.data?.data?.todaysWorkouts);
      console.log(res.data);
      setLoading(false);
    });
  };

  const addNewWorkout = async () => {
    setButtonLoading(true);
    const token = localStorage.getItem('fitness-exercise-tracker-token');
    await addWorkout(token as string, {
      workoutString: workout,
    })
      .then((_res) => {
        dashboardData();
        getTodaysWorkout();
        setButtonLoading(false);
      })
      .catch((err) => {
        alert(err);
      });
  };

  useEffect(() => {
    dashboardData();
    getTodaysWorkout();
  }, []);

  return (
    <Container>
      {loading ? (
        <div>Loading...</div>
      ) : (
        <Wrapper>
          <Title>Dashboard</Title>
          <FlexWrap>
            {counts.map((item) => (
              <CountsCard item={item} data={data} />
            ))}
          </FlexWrap>

          <FlexWrap>
            <WeeklyStatCard data={data} />
            <CategoryChart data={data} />
            <AddWorkout
              workout={workout}
              setWorkout={setWorkout}
              addNewWorkout={addNewWorkout}
              buttonLoading={buttonLoading}
            />
          </FlexWrap>

          <Section>
            <Title>Todays Workouts</Title>
            <CardWrapper>
              {todaysWorkouts.map((workout) => (
                <WorkoutCard workout={workout} />
              ))}
            </CardWrapper>
          </Section>
        </Wrapper>
      )}
    </Container>
  );
};

export default DashboardPage;
